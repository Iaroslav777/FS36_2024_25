// Імпортуємо вбудований модуль 'http' для створення веб-сервера
const http = require("http");

// Визначаємо порт, на якому буде працювати сервер
const PORT = 3000;

// Створюємо екземпляр сервера
// Функція-колбек виконується при кожному вхідному HTTP-запиті
const server = http.createServer((req, res) => {
  // Встановлюємо заголовок відповіді:
  // Content-Type: text/plain вказує, що ми відправляємо простий текст
  // Charset: utf-8 забезпечує коректне відображення українських символів
  res.writeHead(200, { "Content-Type": "text/plain; charset=utf-8" });

  // Відправляємо тіло відповіді
  res.end("Привіт, світ! Це мій перший Node.js сервер!\n");
});

// Запускаємо сервер, щоб він почав слухати вхідні запити на вказаному порті
server.listen(PORT, () => {
  // Цей колбек виконується, коли сервер успішно запущений
  console.log(`Сервер запущено на http://localhost:${PORT}/`);
  console.log("Відкрийте цей URL у вашому браузері, щоб побачити відповідь.");
});
